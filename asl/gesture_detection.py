def draw_bounding_rect(use_brect, image, brect):
    """
    This function draws a rectangle around the bounding box

    :param use_brect: Boolean value that determines whether to draw the bounding box or not
    :param image: The image to draw the bounding box on
    :param brect: The bounding rectangle of the hand
    :return: The image with the bounding box drawn on it.
    """
    if use_brect:
        # Outer rectangle
        cv.rectangle(image, (brect[0], brect[1]), (brect[2], brect[3]),
                     (0, 255, 0), 1)

    return image


def draw_info_text(image, brect, handedness, hand_sign_text):
    """
    This function draws a rectangle on the image, then writes the text to the rectangle

    :param image: The image to draw the text on
    :param brect: The bounding rectangle of the hand
    :param handedness: The classification of the hand as left or right
    :param hand_sign_text: The text that will be displayed on the screen
    :return: The image with the drawn information on it.
    """
    cv.rectangle(image, (brect[0], brect[1]), (brect[2], brect[1] - 22),
                 (0, 0, 0), -1)

    info_text = handedness.classification[0].label[0:]
    if hand_sign_text != "":
        info_text = info_text + ':' + hand_sign_text
    cv.putText(image, info_text, (brect[0] + 5, brect[1] - 4),
               cv.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 1, cv.LINE_AA)

    return image


def draw_info(image, fps, mode, number):
    """
    This function draws the FPS and the mode of the program to the screen

    :param image: The image to draw on
    :param fps: The number of frames per second that the program is running at
    :param mode: The mode of the program
    :param number: The number of the key point you want to log
    :return: The image with the text drawn on it.
    """
    cv.putText(image, "FPS:" + str(fps), (10, 30), cv.FONT_HERSHEY_SIMPLEX,
               1.0, (0, 0, 0), 4, cv.LINE_AA)
    cv.putText(image, "FPS:" + str(fps), (10, 30), cv.FONT_HERSHEY_SIMPLEX,
               1.0, (255, 255, 255), 2, cv.LINE_AA)

    mode_string = ['Logging Key Point']
    if 1 <= mode <= 2:
        cv.putText(image, "MODE:" + mode_string[mode - 1], (10, 90),
                   cv.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 1,
                   cv.LINE_AA)
        if 0 <= number <= 9:
            cv.putText(image, "NUM:" + str(number), (10, 110),
                       cv.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 1,
                       cv.LINE_AA)
    return image


