def draw_bounding_rect(use_brect, image, brect):
    """
    This function draws a rectangle around the bounding box

    :param use_brect: Boolean value that determines whether to draw the bounding box or not
    :param image: The image to draw the bounding box on
    :param brect: The bounding rectangle of the hand
    :return: The image with the bounding box drawn on it.
    """
    if use_brect:
        # Outer rectangle
        cv.rectangle(image, (brect[0], brect[1]), (brect[2], brect[3]),
                     (0, 255, 0), 1)

    return image


