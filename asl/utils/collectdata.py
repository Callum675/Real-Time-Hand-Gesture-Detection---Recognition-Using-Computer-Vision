def get_args():
    """
    The function `get_args()` is used to get the arguments from the command line
    :return: The arguments that are being passed in.
    """
    parser = argparse.ArgumentParser()

    # Camera Information
    parser.add_argument("--device", type=int, default=0)
    parser.add_argument("--width", help='cap width', type=int, default=960)
    parser.add_argument("--height", help='cap height', type=int, default=540)

    # Mediapipe Information
    parser.add_argument('--use_static_image_mode', action='store_true')
    parser.add_argument("--min_detection_confidence",
                        help='min_detection_confidence',
                        type=float,
                        default=0.7)
    parser.add_argument("--min_tracking_confidence",
                        help='min_tracking_confidence',
                        type=int,
                        default=0.5)

    args = parser.parse_args()
    return args


def writeLabel(label):
    row_number = None
    found_label = False
    with open('model/keypoint_classifier/keypoint_classifier_label.csv', 'r', newline='') as f:
        csv_reader = csv.reader(f)
        for i, row in enumerate(csv_reader):
            # Check if the label is present in the current row
            if label.strip().upper() in [cell.strip().upper() for cell in row]:
                row_number = i  # If so, record the index of the row
                found_label = True
                break

    if not found_label:  # If the label was not found in any row...
        with open('model/keypoint_classifier/keypoint_classifier_label.csv', 'a', newline='') as f:
            csv_writer = csv.writer(f)
            # ...append a new row containing the label...
            csv_writer.writerow([label])
            # ...and record the index of the newly added row.
            row_number = csv_reader.line_num

    # Return the index of the row containing the label (whether it was pre-existing or newly added).
    return row_number


def collect_data(num_frames, frame_interval, label):
    """
    Collects hand landmarks from the camera and saves them to a CSV file.

    Args:
        num_frames (int): The number of frames to capture.
        frame_interval (float): The time between each frame capture in seconds.
        output_file (str): The path to the CSV file to save the landmarks to.
    """
    # Argument Parsing
    args = get_args()

    cap_device = args.device
    cap_width = args.width
    cap_height = args.height

    use_static_image_mode = args.use_static_image_mode
    min_detection_confidence = args.min_detection_confidence
    min_tracking_confidence = args.min_tracking_confidence

    use_brect = True

    # Camera Preparation
    cap = cv.VideoCapture(cap_device)
    cap.set(cv.CAP_PROP_FRAME_WIDTH, cap_width)
    cap.set(cv.CAP_PROP_FRAME_HEIGHT, cap_height)

    # Set Mediapipe
    mp_hands = mp.solutions.hands
    hands = mp_hands.Hands(
        static_image_mode=use_static_image_mode,
        max_num_hands=1,
        min_detection_confidence=min_detection_confidence,
        min_tracking_confidence=min_tracking_confidence,
    )
    data = 0
    start_time = time.time()
    i = 0

    while (i < num_frames):
        elapsed_time = time.time() - start_time
        # Camera capture
        ret, image = cap.read()
        if not ret:
            break
        image = cv.flip(image, 1)  # Mirror Display
        debug_image = copy.deepcopy(image)

        # Detection Implementation
        image = cv.cvtColor(image, cv.COLOR_BGR2RGB)

        image.flags.writeable = False
        results = hands.process(image)
        image.flags.writeable = True

        # Post-process Result
        if results.multi_hand_landmarks is not None:
            # get number of hands
            number_of_hands = len(results.multi_handedness)
            for hand_landmarks, handedness in zip(results.multi_hand_landmarks, results.multi_handedness):
                # print(handedness)
                # Bounding Box Calculation
                brect = calc_bounding_rect(debug_image, hand_landmarks)
                # Landmark Calculation
                landmark_list = calc_landmark_list(debug_image, hand_landmarks)
                # Conversion to relative coordinates / normalized coordinates
                data = pre_process_landmark(landmark_list)
                # Drawing Annotations
                debug_image = draw_bounding_rect(use_brect, debug_image, brect)
                debug_image = draw_landmarks(debug_image, landmark_list)
                debug_image = draw_info_text(debug_image, brect, handedness)

                print(str(elapsed_time) + "/" + str(frame_interval))
                # Check if it's time to collect data
                if (elapsed_time >= float(frame_interval)):
                    # Write gesture label to CSV file
                    number = writeLabel(label)

                    # Write landmarks to CSV file
                    log = logging_csv(number, data)
                    i += 1
                    elapsed_time = elapsed_time - elapsed_time

        # Screen Reflection
        cv.imshow('Hand Gesture Recognition', debug_image)

        # Add waitKey to handle events
        if cv.waitKey(1) & 0xFF == ord('q'):
            break

    # Closing Program
    cap.release()
    cv.destroyAllWindows()


def pre_process_landmark(landmark_list):
    """
    The function takes a list of landmarks, converts them to relative coordinates, converts them to a
    one-dimensional list, and normalizes them

    :param landmark_list: a list of tuples, each tuple is a landmark point
    :return: A list of normalized landmarks
    """
    temp_landmark_list = copy.deepcopy(landmark_list)

    # Convert to relative coordinates
    base_x, base_y = 0, 0
    for index, landmark_point in enumerate(temp_landmark_list):
        if index == 0:
            base_x, base_y = landmark_point[0], landmark_point[1]

        temp_landmark_list[index][0] = temp_landmark_list[index][0] - base_x
        temp_landmark_list[index][1] = temp_landmark_list[index][1] - base_y

    # Convert to a one-dimensional list
    temp_landmark_list = list(
        itertools.chain.from_iterable(temp_landmark_list))

    # Normalization
    max_value = max(list(map(abs, temp_landmark_list)))

    def normalize_(n):
        return n / max_value

    temp_landmark_list = list(map(normalize_, temp_landmark_list))

    return temp_landmark_list


def calc_bounding_rect(image, landmarks):
    """
    This function takes an image and a set of landmarks, and returns the bounding box of the landmarks

    :param image: The image to be processed
    :param landmarks: The landmarks to use for the bounding box
    :return: The bounding box coordinates
    """
    image_width, image_height = image.shape[1], image.shape[0]

    landmark_array = np.empty((0, 2), int)

    for _, landmark in enumerate(landmarks.landmark):
        landmark_x = min(int(landmark.x * image_width), image_width - 1)
        landmark_y = min(int(landmark.y * image_height), image_height - 1)
        landmark_point = [np.array((landmark_x, landmark_y))]
        landmark_array = np.append(landmark_array, landmark_point, axis=0)

    x, y, w, h = cv.boundingRect(landmark_array)

    return [x, y, x + w, y + h]


def calc_landmark_list(image, landmarks):
    """
    > The function takes in an image and a landmark object, and returns a list of landmark points

    :param image: The image to be processed
    :param landmarks: The landmarks of the hand
    :return: the landmark points of the hand.
    """
    image_width, image_height = image.shape[1], image.shape[0]

    landmark_point = []

    # Keypoint
    for _, landmark in enumerate(landmarks.landmark):
        landmark_x = min(int(landmark.x * image_width), image_width - 1)
        landmark_y = min(int(landmark.y * image_height), image_height - 1)
        # landmark_z = landmark.z

        landmark_point.append([landmark_x, landmark_y])

    return landmark_point


